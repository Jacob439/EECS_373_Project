uint8_t lora_infINIT(int init_ctr) {
	if (init_ctr < 100) {
		uint8_t res = lora_init(&lora, &hspi1, GPIOB, GPIO_PIN_0, LORA_BASE_FREQUENCY_US+FREQ_OFFSET);
		if (res != LORA_OK) {
			// Initialization failed
			// retry lol
			return lora_infINIT(init_ctr + 1);
	  	} else {
	  		return LORA_OK;
	  	}
	} else {
		return LORA_ERROR;
	}
}

